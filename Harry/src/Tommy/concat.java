package Tommy;

public class concat {
	
	//CONCAT - is all about CLUBING characters.
	

	public concat() {

	}

	public static void main(String[] args) {

		
		String firstname = "samantha";
		String secondname = "nagchaithanya";
		
		String rossy = "tom";
		String mona = "jerry";
		
		System.out.println(rossy.concat(mona));
		
		System.out.println(firstname.concat(secondname));
		
		System.out.println(mona.concat(rossy));
		System.out.println(rossy.concat(secondname));
		
		String med = "sona";
		System.out.println(mona.concat(med));
		
		
		
	String don = "mall";
	System.out.println(don.replace("ma", "se"));
		
		

	}

}

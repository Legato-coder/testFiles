package Arraylist;

import java.util.ArrayList;
import java.util.List;

public class ContainsMethod {
	public static void main(String[] args) {
		
		
List<String> ab=new ArrayList<String>();
		
		ab.add("hyderabad");
		ab.add("sachin");
		ab.add("ananthapur");
		ab.add("mumbai");
		
		System.out.println(ab.contains("mumbai"));
		//contains method will check whether the give value is present or not.
		
		
	}
	

}
